#!/usr/bin/env bash

##############################################################################
# Enterprise Runtime
#
# Automatic Rollback
#
##############################################################################

set -Eeuo pipefail

##############################################################################
# Runtime
##############################################################################

RUNTIME_DIR="/opt/runtime"

COMPOSE_FILE="${RUNTIME_DIR}/compose.yaml"

LAST_GOOD_ENV="${RUNTIME_DIR}/last-good.env"

##############################################################################
# Logging
##############################################################################

log() {

    echo
    echo "=================================================="
    echo "$1"
    echo "=================================================="

}

##############################################################################
# Validate
##############################################################################

if [[ ! -f "${LAST_GOOD_ENV}" ]]; then

    log "Rollback aborted"

    echo "last-good.env not found."

    exit 1

fi

##############################################################################
# Stop current deployment
##############################################################################

log "Stopping failed deployment"

docker compose \
    --env-file "${LAST_GOOD_ENV}" \
    -f "${COMPOSE_FILE}" \
    down \
    --remove-orphans || true

##############################################################################
# Restore previous version
##############################################################################

log "Restoring previous stable version"

docker compose \
    --env-file "${LAST_GOOD_ENV}" \
    -f "${COMPOSE_FILE}" \
    up -d

##############################################################################
# Status
##############################################################################

log "Rollback status"

docker compose \
    --env-file "${LAST_GOOD_ENV}" \
    -f "${COMPOSE_FILE}" \
    ps

##############################################################################
# Finished
##############################################################################

log "Rollback completed successfully"